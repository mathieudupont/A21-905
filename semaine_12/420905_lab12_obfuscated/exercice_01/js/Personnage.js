// Classe Personnage.
/** Propriétés :
 *  nom : Nom du personnage
 *  positionX : Position horizontale dans la page.
 *  positionY : Position verticale dans la page.
 */
class Personnage {
    constructor(nom, positionX, positionY){
        this.nom = nom;
        this.positionX = positionX;
        this.positionY = positionY;
    }
}